export function getFileIconComponent(name: string, isDir: boolean, isExpanded?: boolean): React.ReactNode {
  if (isDir) return <FolderIcon open={isExpanded} />;
  const ext = name.split('.').pop()?.toLowerCase() ?? '';
  const lower = name.toLowerCase();
  if (ext === 'c') return <FileIconC />;
  if (ext === 'h') return <FileIconH />;
  if (ext === 'exe') return <FileIconExe />;
  if (ext === 'o' || ext === 'obj') return <FileIconObj />;
  if (ext === 'dll' || ext === 'lib' || ext === 'a') return <FileIconDll />;
  if (ext === 'py') return <FileIconPy />;
  if (ext === 'bat' || ext === 'cmd' || ext === 'sh') return <FileIconBat />;
  if (ext === 'md' || ext === 'markdown') return <FileIconMd />;
  if (ext === 'json' || ext === 'xml' || ext === 'yaml' || ext === 'yml') return <FileIconJson />;
  if (ext === 'txt') return <FileIconTxt />;
  if (ext === 'png' || ext === 'jpg' || ext === 'jpeg' || ext === 'gif' || ext === 'bmp' || ext === 'ico' || ext === 'svg') return <FileIconImage />;
  if (ext === 'zip' || ext === 'rar' || ext === 'gz' || ext === '7z' || ext === 'tar') return <FileIconZip />;
  if (lower === 'makefile' || lower === 'cmake' || ext === 'mk') return <FileIconMake />;
  return <FileIconDefault />;
}
